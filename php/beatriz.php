        <h1 class="title">BEATRIZ SOUZA</h1>
        <p class="info">A brasileira Beatriz Souza é campeã olímpica do judô nas Olimpíadas de Paris 2024, na categoria "acima de 78kg", em sua primeira participação nos Jogos. Ela, que ocupa a quinta posição no ranking mundial, venceu a israelense Raz Hershko na decisão da medalha de ouro. Nascida em Peruíbe, litoral de São Paulo, Beatriz Souza, ou "Bia", tem 26 anos de idade, tem 1,78m de altura e pesa cerca de 135kg (foi a terceira mais pesada da categoria em Paris). Atleta o clube Pinheiros, em São Paulo, ela é presença constante nos pódios das principais competições do esporte. Em Olimpíadas, a brasileira se tornou a primeira mulher brasileira estreante nos Jogos a ser campeã olímpica em provas individuais.</p>
