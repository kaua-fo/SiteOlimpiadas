        <h1 class="title">FLÁVIA SARAIVA</h1>
        <p class="info">lávia Lopes Saraiva nasceu no Rio de Janeiro, no dia 30 de setembro de 1999. O sonho de ser ginasta começou ainda menina quando assistia às apresentações de Daiane Hipólito e Daiane dos Santos. Treinando em um projeto social para alunos de escolas públicas, foi descoberta aos oito anos de idade. Com apenas 1,33m de altura, é considerada o novo fenômeno da ginástica artística. Com 11 anos de idade, mesmo com o receio da mãe, resolveu deixar a Zona Norte carioca e ir morar em Três Rios, no sul do Estado, com o técnico e outros atletas, no projeto criado pela técnica Georgette Vidor. Para ser competitiva a atleta começou a treinar durante seis ou sete horas por dia e logo se destacou nas competições.</p>
