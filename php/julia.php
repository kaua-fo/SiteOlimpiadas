        <p class="info">Júlia se tornou mais conhecida do público nos Jogos Olímpicos de Paris, devido à sua apresentação no solo, mas ela começou sua história com a ginástica há alguns anos, no Centro de Excelência em Ginástica do Paraná, em Curitiba. Ali mesmo, ela chamou a atenção da treinadora ucraniana Iryna Ilyashenko, que a levou para treinar com as atletas de ginástica com potencial para representar a Seleção Brasileira. Desde 2018, Júlia soma alguns resultados importantes. Ela foi campeã Sul-Americana Júnior na trave, ficou em quinto lugar no individual geral do Campeonato Brasileiro de Aparelhos, e levou o bronze na trave e no solo. Depois, ela entrou para a Seleção no Campeonato Mundial Juvenil. Em 2022, Júlia conquistou sua primeira medalha de ouro de peso na etapa de Baku da Copa do Mundo de Ginástica Artística, além de ter subido ao topo do pódio quatro vezes no Sul-Americano da modalidade.</p>
